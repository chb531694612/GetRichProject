def _parse_now(raw: str | None, settings: Settings) -> datetime:
    if not raw:
        return datetime.now(settings.timezone)
    parsed = datetime.fromisoformat(raw.replace("Z", "+00:00"))
    if parsed.tzinfo is None:
        return parsed.replace(tzinfo=settings.timezone)
    return parsed.astimezone(settings.timezone)


def _build_service(settings: Settings) -> ScoreFourfoldService:
    database = Database(settings.database_path)
    database.initialize()
    return ScoreFourfoldService(settings, database, build_provider(settings), Mailer(settings))


def _run_recorded(service: ScoreFourfoldService, job_name: str, now: datetime, action) -> JobOutcome:
    started_at = datetime.now(service.settings.timezone)
    try:
        outcome = action(now)
    except Exception as exc:
        finished_at = datetime.now(service.settings.timezone)
        service.database.record_job(job_name, started_at, finished_at, "error", f"{type(exc).__name__}: {exc}")
        service.enqueue_error(job_name, exc, finished_at)
        raise
    finished_at = datetime.now(service.settings.timezone)
    service.database.record_job(job_name, started_at, finished_at, outcome.status, outcome.detail)
    return outcome


def _run_cycle(
    service: ScoreFourfoldService,
    now: datetime,
    *,
    include_recommend: bool = True,
    recommend_job_name: str = "recommend",
    include_finalize: bool = False,
    finalize_job_name: str = "recommend-finalize",
) -> tuple[list[JobOutcome], list[str]]:
    outcomes: list[JobOutcome] = []
    errors: list[str] = []
    actions = [("settle", service.settle)]
    if include_finalize:
        actions.insert(0, (finalize_job_name, service.finalize_recommendation_day))
    if include_recommend:
        actions.insert(0, (recommend_job_name, service.recommend))
    for name, action in actions:
        try:
            outcome = _run_recorded(service, name, now, action)
        except Exception as exc:
            error = f"{name}: {type(exc).__name__}: {exc}"
            LOGGER.exception("%s failed", name)
            errors.append(error)
        else:
            outcomes.append(outcome)
            LOGGER.info("%s: %s", name, outcome.detail)
    # Send recommendation, settlement and error notifications queued in this cycle.
    try:
        outcome = _run_recorded(service, "send-mail", now, service.send_mail)
    except Exception as exc:
        error = f"send-mail: {type(exc).__name__}: {exc}"
        LOGGER.exception("send-mail failed")
        errors.append(error)
    else:
        outcomes.append(outcome)
        LOGGER.info("send-mail: %s", outcome.detail)
    return outcomes, errors


def _build_manual_trigger(
    service: ScoreFourfoldService,
    wake_event: threading.Event,
):
    """Build an idempotent dashboard action using the normal recommendation path."""

    def trigger(request_id: str) -> tuple[str, str]:
        started_at = service.now()
        if not service.database.claim_web_request(request_id, started_at):
            previous = service.database.get_web_request(request_id)
            if previous is None:
                return "cooldown", "手动操作间隔至少5分钟，请稍后再试"
            if previous[0] == "running":
                return "busy", "本次手动推荐正在执行，请稍后刷新页面查看结果"
            return previous

        status = "error"
        detail = "手动推荐执行异常，错误通知已进入邮件队列"
        try:
            outcome = _run_recorded(
                service,
                f"manual-recommend:{request_id}",
                started_at,
                service.try_recommend,
            )
        except Exception:
            LOGGER.exception("manual recommendation failed")
        else:
            status, detail = outcome.status, outcome.detail

        finished_at = service.now()
        service.database.finish_web_request(request_id, status, detail, finished_at)

        # A manual attempt replaces the latest due scheduled attempt. Recording
        # the slot prevents an immediate second provider request when the daemon
        # wakes to deliver mail generated by this action.
        if status != "busy":
            slot = due_recommendation_slot(
                started_at,
                service.settings.recommendation_times,
                service.settings.recommendation_latest_start,
                service.database.has_job_run,
            )
            if slot is not None:
                service.database.record_job(
                    slot_job_name(started_at, slot),
                    started_at,
                    finished_at,
                    f"manual-{status}",
                    detail,
                )
            wake_event.set()
        return status, detail

    return trigger


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="score-fourfold",
        description="竞彩足球明确比分4串1的2元基准推荐与自动结算服务",
    )
