def main(argv: list[str] | None = None) -> None:
    parser = build_parser()
    args = parser.parse_args(argv)
    if args.command == "hash-password":
        if args.stdin:
            password = sys.stdin.readline().rstrip("\r\n")
        else:
            password = getpass.getpass("请输入网页登录密码（至少16个字符）：")
            confirmation = getpass.getpass("请再次输入：")
            if password != confirmation:
                parser.error("两次输入的密码不一致")
        try:
            print(hash_password(password))
        except ValueError as exc:
            parser.error(str(exc))
        return
    load_dotenv(Path(args.env_file))
    logging.basicConfig(
        level=logging.DEBUG if args.verbose else logging.INFO,
        format="%(asctime)s %(levelname)s %(name)s %(message)s",
    )
    try:
        settings = Settings.from_env()
    except Exception as exc:
        parser.error(f"配置错误：{exc}")
        return
    service = _build_service(settings)

    if args.command == "init-db":
        print(f"数据库已初始化：{settings.database_path}")
        return
    if args.command == "check-config":
        output = _safe_settings(settings)
        output["mail_errors"] = settings.validate_mail()
        print(json.dumps(output, ensure_ascii=False, indent=2))
        return
    if args.command == "status":
        print(json.dumps(service.database.summary(), ensure_ascii=False, indent=2))
        return
    if args.command == "health":
        summary = service.database.summary()
        issues = settings.validate_mail()
        if int(summary.get("emails_dead", 0)) > 0:
            issues.append(f"存在{summary['emails_dead']}封邮件死信")
        output = {"healthy": not issues, "issues": issues, "summary": summary}
        print(json.dumps(output, ensure_ascii=False, indent=2))
        if issues:
            raise SystemExit(1)
        return

    now = _parse_now(getattr(args, "now", None), settings)
    try:
        if args.command == "recommend":
            outcome = _run_recorded(service, "recommend", now, service.recommend)
            print(outcome.detail)
        elif args.command == "settle":
            outcome = _run_recorded(service, "settle", now, service.settle)
            print(outcome.detail)
        elif args.command == "send-mail":
            outcome = _run_recorded(service, "send-mail", now, service.send_mail)
            print(outcome.detail)
        elif args.command == "test-mail":
            outcome = _run_recorded(service, "test-mail", now, service.test_mail)
            print(outcome.detail)
        elif args.command == "run-cycle":
            actual_now = datetime.now(settings.timezone)
            include_finalize = (
                actual_now.timetz().replace(tzinfo=None) >= settings.recommendation_deadline
            )
            outcomes, errors = _run_cycle(
                service,
                now,
                include_finalize=include_finalize,
                finalize_job_name=f"recommend-finalize:{actual_now:%Y%m%d}",
            )
            for outcome in outcomes:
                print(outcome.detail)
            if errors:
                raise SystemExit("\n".join(errors))
        elif args.command == "probe-data":
            matches = service.provider.get_matches()
            results = service.provider.get_results((now - timedelta(days=2)).date(), now.date())
            counts: dict[str, int] = {}
            for result in results.values():
                counts[result.status.value] = counts.get(result.status.value, 0) + 1
            print(
                json.dumps(
                    {
                        "matches": len(matches),
                        "complete_selling_crs_matches": sum(
                            match.betting_all_up and len(match.score_options) == 31 for match in matches
                        ),
                        "complete_selling_had_matches": sum(
                            match.had_betting_all_up and len(match.had_options) == 3 for match in matches
                        ),
                        "results": len(results),
                        "result_statuses": counts,
                    },
                    ensure_ascii=False,
                    indent=2,
                )
            )
        elif args.command == "probe-ai":
            print(json.dumps({"ok": True, "response": probe_deepseek(settings)}, ensure_ascii=False))
        elif args.command == "daemon":
            stop_event = threading.Event()
            wake_event = threading.Event()
            dashboard_server: DashboardServer | None = None

            def stop(*_):
                stop_event.set()
                wake_event.set()

            signal.signal(signal.SIGINT, stop)
            if hasattr(signal, "SIGTERM"):
                signal.signal(signal.SIGTERM, stop)
            try:
                if settings.web_enabled:
                    application = DashboardApplication(
                        settings,
                        service.database,
                        _build_manual_trigger(service, wake_event),
                    )
                    dashboard_server = DashboardServer(settings, application)
                    dashboard_server.start()
                    host, port = dashboard_server.address
                    access_note = (
                        f"由HTTPS网关保护，公开地址 {settings.web_public_origin}"
                        if settings.web_access_mode == "public"
                        else "请仅通过SSH隧道访问"
                    )
                    LOGGER.info("个人看板已监听 %s:%s（%s）", host, port, access_note)

                while not stop_event.is_set():
                    cycle_now = datetime.now(settings.timezone)
                    slot = due_recommendation_slot(
                        cycle_now,
                        settings.recommendation_times,
                        settings.recommendation_latest_start,
                        service.database.has_job_run,
                    )
                    recommend_job_name = (
                        slot_job_name(cycle_now, slot) if slot is not None else "recommend"
                    )
                    finalize_job_name = f"recommend-finalize:{cycle_now:%Y%m%d}"
                    include_finalize = (
                        cycle_now.timetz().replace(tzinfo=None) >= settings.recommendation_deadline
                        and not service.database.has_job_run(finalize_job_name)
                    )
                    _run_cycle(
                        service,
                        cycle_now,
                        include_recommend=slot is not None,
                        recommend_job_name=recommend_job_name,
                        include_finalize=include_finalize,
                        finalize_job_name=finalize_job_name,
                    )
                    if args.once:
                        break
                    wait_seconds = seconds_until_next_event(
                        datetime.now(settings.timezone),
                        settings.recommendation_times,
                        settings.recommendation_deadline,
                        settings.poll_interval_seconds,
                    )
                    wake_event.wait(wait_seconds)
                    wake_event.clear()
            finally:
                if dashboard_server is not None:
                    dashboard_server.stop()
    except KeyboardInterrupt:
        LOGGER.info("收到停止信号")
    except Exception as exc:
        LOGGER.error("%s", exc)
        raise SystemExit(1) from exc


if __name__ == "__main__":
    main(sys.argv[1:])
